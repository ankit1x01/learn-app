import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const contentDir = path.join(__dirname, '../src/data/game-content');
const outputFile = path.join(__dirname, '../src/data/bundled-game-content.ts');

function bundleGameContent() {
  const bundledContent = {};

  // Create directory if it doesn't exist
  if (!fs.existsSync(contentDir)) {
    fs.mkdirSync(contentDir, { recursive: true });
  }

  // Read all JSON files in game-content directory
  if (fs.existsSync(contentDir)) {
    const files = fs.readdirSync(contentDir).filter(f => f.endsWith('.json'));

    files.forEach(file => {
      const filePath = path.join(contentDir, file);
      const gameType = path.basename(file, '.json');

      try {
        const rawContent = fs.readFileSync(filePath, 'utf-8');
        const content = JSON.parse(rawContent);
        bundledContent[gameType] = Array.isArray(content) ? content : [content];
        console.log(`✓ Bundled ${file} (${bundledContent[gameType].length} items)`);
      } catch (error) {
        console.error(`✗ Error parsing ${file}: ${error.message}`);
      }
    });
  }

  // Generate TypeScript file
  const tsContent = `// Auto-generated by scripts/bundle-game-content.mjs
// DO NOT EDIT MANUALLY
import { GameContent } from '@/lib/game-content-store';

export const bundledGameContent: Record<string, GameContent[]> = ${JSON.stringify(bundledContent, null, 2)};

export function getBundledContent(gameType: string): GameContent[] {
  return bundledGameContent[gameType] ?? [];
}
`;

  fs.writeFileSync(outputFile, tsContent, 'utf-8');
  console.log(`\n✓ Generated ${outputFile}`);
}

bundleGameContent();
